public class Boxing_1 {
    public static void main(String[] args) {
        Integer ob1=Integer.valueOf(100);
        int ob2=ob1.intValue();

        System.out.println("IntegerObject 1 : "+ob1+" IntegerPermeative 1 : "+ob2);
        
        Boolean ob3=Boolean.valueOf(true);
        boolean ob4=ob3.booleanValue();
        
        System.out.println("BooleanObject 1 : "+ob3+" IntegerPermeative 1 : "+ob4);
        
    }
}
