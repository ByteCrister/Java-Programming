public class d_type {
    public static void main(String args[]){
        //integral data types
        //---------------------------------

        //integer
        int _integer=12;
        short s_type=12344;
        long l_type=12123456l;
        byte b_type=-123;

        //floating datatype
        float floating=12.12f;
        double d_float=12.123456d; 

        //character datatype
        char ch='S';

        //---------------------------------

        //boolean data type
        boolean var_1=true;
        boolean var_2=false;


        //printing all the stufs

        System.out.println(_integer);
        System.out.println(s_type);
        System.out.println(l_type);
        System.out.println(b_type);
        System.out.println(floating);
        System.out.println(d_float);
        System.out.println(ch);
        System.out.println(var_1);
        System.out.println(var_2);



    }
}
